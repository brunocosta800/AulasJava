package aula03.exemplos;

public class Exemplos3 {
    public static void main(String[] args) {
        int num1 = 8, num2 = 7;
        boolean resposta;

        resposta = num1 > num2;
        System.out.println(resposta);

        resposta = num1 < num2;
        System.out.println(resposta);

        resposta = num1 >= num2;
        System.out.println(resposta);

        resposta = num1 <= num2;
        System.out.println(resposta);

        resposta = num1 == num2;
        System.out.println(resposta);

        resposta = num1 != num2;
        System.out.println(resposta);
    }
}
